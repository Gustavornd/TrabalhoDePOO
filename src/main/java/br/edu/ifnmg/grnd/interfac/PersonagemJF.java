    private void btnBuscaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscaActionPerformed
        PersonagemDAO persD = new PersonagemDAO();
        InventarioDAO invetD = new InventarioDAO();
        Long i = 1L;
        
        while(persD.localizarPorId(i) != null){
            
            if(persD.localizarPorId(i).getNome() == null ? txtNomeBusca.getText() == null : persD.localizarPorId(i).getNome().equals(txtNomeBusca.getText())){
                txtNome.setText("" + persD.localizarPorId(i).getNome());
                String S = String.valueOf(persD.localizarPorId(i).getLimPeso());
                txtLimite.setText(S);
                String S1 = String.valueOf(persD.localizarPorId(i).getDinheiro());
                txtDinheiro.setText(S1);
                String S2 = String.valueOf(persD.localizarPorId(i).getCa());
                txtCA.setText(S2);
                Long L = Long.valueOf(persD.localizarPorId(i).getInventario());
                String S3 = String.valueOf(invetD.localizarPorId(L).getTotalitems());
                txtItens.setText(S3);
            }
            i++;
        }
    }//GEN-LAST:event_btnBuscaActionPerformed

