package br.edu.ifnmg.grnd.entidade;

import br.edu.ifnmg.grnd.entidade.Entidade;
import java.util.Objects;

/**
 * Usuario para acesso ao sistema
 * 
 * @author Gustavo Rafael Nunes Durães
 * @author Lucas Flávio Gabrich Marinho
 * @author Friedrich Naum Espindola Loiola Costa
 */
public class Usuario extends Entidade{
    private String nomeSistema;
    private String senha;
    private Boolean administrador;

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 32 * hash + Objects.hashCode(this.senha);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Usuario other = (Usuario) obj;
        return true;
    }

    //<editor-fold defaultstate="collapsed" desc="Constructor">
    public Usuario() {
        administrador = false;
    }
    //</editor-fold>

    //<editor-fold defaultstate="collapsed" desc="Getters/Setters">
    public String getNomeSistema() {
        return nomeSistema;
    }

    public void setNomeSistema(String nomeSistema) {
        this.nomeSistema = nomeSistema;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }

    public Boolean getAdministrador() {
        return administrador;
    }

    public void setAdministrador(Boolean administrador) {
        this.administrador = administrador;
    }
    //</editor-fold>
    
}
